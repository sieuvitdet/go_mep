class UserData {
  final String name;
  final String avatarUrl;
  final String id;
  final String phone;
  final String email;
  final String address;
  final String position;
  final String department;

  UserData(
      {required this.name,
      required this.avatarUrl,
      required this.id,
      required this.phone,
      required this.email,
      required this.address,
      required this.position,
      required this.department});
}
